class ADOrder
{
	public static void main(String args[])
	{
		int n=Integer.parseInt(args[0]);
		for(int i=0;i<=n;i++)
		System.out.println("Ascending order is" +i);
		for(int i=n;i>=0;i--)
		System.out.println("descending order is"+i);
		
	}
}
