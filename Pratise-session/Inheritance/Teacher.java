class Teacher
{
	int id;
	String name;
	float sal;
	public void setId(int id)
	{
		this.id=id;
	}
	public int getId()
	{
		return id;
	}
	public void setName(String name)
	{
		this.name=name;
	}
	public String getName()
	{
		return name;
	}
	public void setSal(float sal)
	{
		this.sal=sal;
	}
	public float getSal()
	{
		return sal;
	}
}
